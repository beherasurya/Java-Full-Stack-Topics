 class Switch {
    public static void main(String[] args) {
        
        int x=3;

        switch(x)
        {
                case 1:
                    System.out.println("The value of X is" +x); 
                    break;
                case 3:
                    System.out.println("Case 3: The value of X is "+x);
                    break;

                    default:
                        System.out.println("The default value of X is"+x);
        }
    }
}
